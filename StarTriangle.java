public class StarTriangle{
           //static int a= 1;
     public static void main(String []args){
        int n=5;
        for(int i=0;i<n;i++)
        {
            int k=i;
        for(int j=0;j<=k;j++)
        {
            System.out.print("* "); 
         //System.out.print(" "+a); 
         //a=a+1;
        }
        System.out.println(""); 
        }
     }
}


